// CSV Export with BOM for Excel compatibility
export function exportToCSV(
  data: Record<string, unknown>[],
  filename: string,
  headers?: string[],
  language: ExportLanguage = 'fr'
): void {
  if (data.length === 0) {
    console.warn('No data to export');
    return;
  }

  const keys = headers || Object.keys(data[0]);
  const delimiter = csvDelimiter[language];
  const decimal = decimalSeparator[language];

  // Create CSV content with BOM for Excel
  const BOM = '\uFEFF';
  const csvContent = [
    keys.join(delimiter), // Header row with locale-appropriate delimiter
    ...data.map(row =>
      keys.map(key => {
        const value = row[key];
        // Handle different types and escape quotes
        if (value === null || value === undefined) return '';
        if (typeof value === 'string') return `"${value.replace(/"/g, '""')}"`;
        if (typeof value === 'number') return value.toString().replace('.', decimal);
        return String(value);
      }).join(delimiter)
    )
  ].join('\n');

  // Create and download file
  const blob = new Blob([BOM + csvContent], { type: 'text/csv;charset=utf-8;' });
  downloadBlob(blob, `${filename}.csv`);
}

// Finance-specific CSV export
export interface FinanceExportData {
  payments: PaymentExportRecord[];
  summary: {
    totalCollected: number;
    totalExpected: number;
    totalOverdue: number;
    collectionRate: number;
  };
  period: string;
}

export interface PaymentExportRecord {
  date: string;
  tenant: string;
  property: string;
  amount: number;
  status: 'paid' | 'pending' | 'overdue';
  paymentMethod?: string;
}

export function exportFinanceToCSV(data: FinanceExportData, language: ExportLanguage = 'fr'): void {
  const t = translations;
  const headers = [
    t.csv.date[language],
    t.csv.tenant[language],
    t.csv.property[language],
    t.csv.amount[language],
    t.csv.status[language],
    t.csv.method[language],
  ];

  const csvData = data.payments.map(p => ({
    [t.csv.date[language]]: p.date,
    [t.csv.tenant[language]]: p.tenant,
    [t.csv.property[language]]: p.property,
    [t.csv.amount[language]]: p.amount,
    [t.csv.status[language]]: t.status[p.status][language],
    [t.csv.method[language]]: p.paymentMethod || '-'
  }));

  // Add summary row
  csvData.push({
    [t.csv.date[language]]: '',
    [t.csv.tenant[language]]: '',
    [t.csv.property[language]]: t.csv.total[language],
    [t.csv.amount[language]]: data.summary.totalCollected,
    [t.csv.status[language]]: `${t.csv.rate[language]}: ${data.summary.collectionRate}%`,
    [t.csv.method[language]]: ''
  });

  exportToCSV(csvData, `finances-${data.period}`, headers, language);
}

// PDF Export using iframe + srcdoc (secure approach)
export function exportFinanceToPDF(data: FinanceExportData, language: ExportLanguage = 'fr'): void {
  const html = generateFinancePDFHTML(data, language);

  // Create hidden iframe for printing
  const iframe = document.createElement('iframe');
  iframe.style.position = 'fixed';
  iframe.style.right = '0';
  iframe.style.bottom = '0';
  iframe.style.width = '0';
  iframe.style.height = '0';
  iframe.style.border = 'none';

  document.body.appendChild(iframe);

  // Use srcdoc attribute (safer than document.write)
  iframe.srcdoc = html;

  // Wait for content to load then print
  iframe.onload = () => {
    setTimeout(() => {
      iframe.contentWindow?.print();
      // Clean up after print dialog closes
      setTimeout(() => {
        document.body.removeChild(iframe);
      }, 1000);
    }, 250);
  };
}

