// Locale mapping for date/number formatting
const localeMap: Record<ExportLanguage, string> = {
  fr: 'fr-FR',
  en: 'en-GB',
  nl: 'nl-NL',
  de: 'de-DE',
};

// CSV delimiter varies by locale (semicolon for FR/DE, comma for EN/NL)
const csvDelimiter: Record<ExportLanguage, string> = {
  fr: ';',
  en: ',',
  nl: ';',
  de: ';',
};

// Decimal separator varies by locale
const decimalSeparator: Record<ExportLanguage, string> = {
  fr: ',',
  en: '.',
  nl: ',',
  de: ',',
};

